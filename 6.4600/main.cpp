#include <iostream>
#include<cmath>

using namespace std;
int main()
{
    double a;
    cout<<"Enter one integer:";
    cin>>a;

    cout<<ceil(a)<<endl;
    cout<<cos(a)<<endl;
    cout<<exp(a)<<endl;
    cout<<fabs(a)<<endl;
    cout<<floor(a)<<endl;

    cout<<log(a)<<endl;
    cout<<log10(a)<<endl;

    cout<<sin(a)<<endl;
    cout<<sqrt(a)<<endl;
    cout<<tan(a)<<endl;
    return 0;
}

